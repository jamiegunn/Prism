namespace Prism.Features.Evaluation.Domain;

/// <summary>
/// Represents a single evaluation result for one record evaluated by one model.
/// </summary>
public sealed class EvaluationResult : BaseEntity
{
    /// <summary>
    /// Gets or sets the parent evaluation ID.
    /// </summary>
    public Guid EvaluationId { get; set; }

    /// <summary>
    /// Gets or sets the model identifier that generated this result.
    /// </summary>
    public string Model { get; set; } = "";

    /// <summary>
    /// Gets or sets the source dataset record ID.
    /// </summary>
    public Guid RecordId { get; set; }

    /// <summary>
    /// Gets or sets the input text sent to the model.
    /// </summary>
    public string Input { get; set; } = "";

    /// <summary>
    /// Gets or sets the expected output from the dataset.
    /// </summary>
    public string? ExpectedOutput { get; set; }

    /// <summary>
    /// Gets or sets the actual output generated by the model.
    /// </summary>
    public string? ActualOutput { get; set; }

    /// <summary>
    /// Gets or sets the scores from each scoring method (key = method name, value = score).
    /// </summary>
    public Dictionary<string, double> Scores { get; set; } = new();

    /// <summary>
    /// Gets or sets the raw logprobs data as a JSON string.
    /// </summary>
    public string? LogprobsData { get; set; }

    /// <summary>
    /// Gets or sets the perplexity score for this result.
    /// </summary>
    public double? Perplexity { get; set; }

    /// <summary>
    /// Gets or sets the inference latency in milliseconds.
    /// </summary>
    public long LatencyMs { get; set; }

    /// <summary>
    /// Gets or sets the number of prompt tokens used.
    /// </summary>
    public int PromptTokens { get; set; }

    /// <summary>
    /// Gets or sets the number of completion tokens generated.
    /// </summary>
    public int CompletionTokens { get; set; }

    /// <summary>
    /// Gets or sets the error message if this record's evaluation failed.
    /// </summary>
    public string? Error { get; set; }
}
