namespace Prism.Features.Evaluation.Domain;

/// <summary>
/// Pluggable interface for scoring model outputs against expected outputs.
/// Each implementation provides a named scoring strategy.
/// </summary>
public interface IScoringMethod
{
    /// <summary>
    /// Gets the unique name of this scoring method (e.g., "exact_match", "rouge_l").
    /// </summary>
    string Name { get; }

    /// <summary>
    /// Scores the actual output against the expected output.
    /// </summary>
    /// <param name="input">The input text sent to the model.</param>
    /// <param name="expected">The expected output from the dataset.</param>
    /// <param name="actual">The actual output generated by the model.</param>
    /// <param name="ct">A token to cancel the operation.</param>
    /// <returns>A score between 0.0 and 1.0 where 1.0 is a perfect match.</returns>
    Task<double> ScoreAsync(string input, string expected, string actual, CancellationToken ct);
}
