using Prism.Common.Database;

namespace Prism.Features.Experiments.Domain;

/// <summary>
/// Represents a single execution of an experiment with specific parameters,
/// capturing input, output, metrics, and performance data.
/// </summary>
public sealed class Run : BaseEntity
{
    /// <summary>
    /// Gets or sets the ID of the experiment this run belongs to.
    /// </summary>
    public Guid ExperimentId { get; set; }

    /// <summary>
    /// Gets or sets the parent experiment.
    /// </summary>
    public Experiment? Experiment { get; set; }

    /// <summary>
    /// Gets or sets the optional display name of the run.
    /// </summary>
    public string? Name { get; set; }

    /// <summary>
    /// Gets or sets the model identifier used for this run.
    /// </summary>
    public string Model { get; set; } = "";

    /// <summary>
    /// Gets or sets the optional inference provider instance ID.
    /// </summary>
    public Guid? InstanceId { get; set; }

    /// <summary>
    /// Gets or sets the inference parameters used for this run.
    /// </summary>
    public RunParameters Parameters { get; set; } = new();

    /// <summary>
    /// Gets or sets the optional prompt version ID if linked to a prompt template.
    /// </summary>
    public Guid? PromptVersionId { get; set; }

    /// <summary>
    /// Gets or sets the input text sent to the model.
    /// </summary>
    public string Input { get; set; } = "";

    /// <summary>
    /// Gets or sets the output text generated by the model.
    /// </summary>
    public string? Output { get; set; }

    /// <summary>
    /// Gets or sets the optional system prompt used for this run.
    /// </summary>
    public string? SystemPrompt { get; set; }

    /// <summary>
    /// Gets or sets custom metrics recorded for this run (e.g., BLEU, ROUGE).
    /// </summary>
    public Dictionary<string, double> Metrics { get; set; } = new();

    /// <summary>
    /// Gets or sets the number of prompt tokens consumed.
    /// </summary>
    public int PromptTokens { get; set; }

    /// <summary>
    /// Gets or sets the number of completion tokens generated.
    /// </summary>
    public int CompletionTokens { get; set; }

    /// <summary>
    /// Gets or sets the total tokens used (prompt + completion).
    /// </summary>
    public int TotalTokens { get; set; }

    /// <summary>
    /// Gets or sets the estimated cost of this run in USD.
    /// </summary>
    public decimal? Cost { get; set; }

    /// <summary>
    /// Gets or sets the total latency of the run in milliseconds.
    /// </summary>
    public long LatencyMs { get; set; }

    /// <summary>
    /// Gets or sets the time to first token in milliseconds.
    /// </summary>
    public int? TtftMs { get; set; }

    /// <summary>
    /// Gets or sets the tokens per second generation rate.
    /// </summary>
    public double? TokensPerSecond { get; set; }

    /// <summary>
    /// Gets or sets the perplexity score for this run.
    /// </summary>
    public double? Perplexity { get; set; }

    /// <summary>
    /// Gets or sets the raw logprobs data as a JSON string.
    /// </summary>
    public string? LogprobsData { get; set; }

    /// <summary>
    /// Gets or sets the execution status of the run.
    /// </summary>
    public RunStatus Status { get; set; } = RunStatus.Pending;

    /// <summary>
    /// Gets or sets the error message if the run failed.
    /// </summary>
    public string? Error { get; set; }

    /// <summary>
    /// Gets or sets the tags associated with this run for filtering.
    /// </summary>
    public List<string> Tags { get; set; } = [];

    /// <summary>
    /// Gets or sets the reason generation finished (e.g., "stop", "length").
    /// </summary>
    public string? FinishReason { get; set; }
}
